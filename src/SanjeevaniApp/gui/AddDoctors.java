        public void loadDoctorsDetails()
{
    try
    {
        doctors = EmpDao.getNonRegisteredDoctortList();
    if(doctors.size()==0){
           JOptionPane.showMessageDialog(null, "No unregistered Receptionist present");
           btnregister.setEnabled(false);
           return;
    }
        btnregister.setEnabled(true);
        Set keys = doctors.keySet();
        Iterator <String> it = keys.iterator();
        jcEmpId.removeAllItems();
        while(it.hasNext())
        {
            jcEmpId.addItem(it.next());
        }
        txtdocid.setText(DoctorsDao.getNewDoctorsId());
    }
    catch(SQLException sql)
    {
        JOptionPane.showMessageDialog(null, "SQL me dikkat ","error",JOptionPane.ERROR_MESSAGE);
           sql.printStackTrace();
    }
}

