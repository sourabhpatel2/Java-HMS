    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        if (!validateInput())
        {
            JOptionPane.showMessageDialog(null, "Please fill all field","Empty values",JOptionPane.ERROR_MESSAGE);
            return;

        }
        try
        {
            String empid = jcEmpId.getSelectedItem().toString();
            String userId = txtuserid.getText();
              String did = txtdocid.getText();
                String qualification = txtqualification.getText();
                    String specialist = txtspecialist.getText();
            char[] pwd = txtpw.getPassword();
            String pass1=String.valueOf(pwd);
            char[] repass = txtrepwd.getPassword();
            String pass2 = String.valueOf(repass);
            if(!passwordMatch(pass1,pass2))
            {
                JOptionPane.showMessageDialog(null, "password do not match","diff values",JOptionPane.ERROR_MESSAGE);
                return;
            }
            UserDetails user = new UserDetails();
            user.setUserid(userId);
            user.setUserName(txtempname.getText());
            user.setEmpId(empid);
            user.setPassword(pass1);
                     
              user.setUserType("DOCTOR");

            boolean result = DoctorsDao.addDoctors(user);
            if (result)
            {
                JOptionPane.showMessageDialog(null, "Record saved to users table!","balle balle",JOptionPane.INFORMATION_MESSAGE);
                clearText();
                loadDoctorsDetails();
                DoctorsPojo d=new DoctorsPojo();
                d.setUserid(userId);
                d.setDoctorid(did);
                d.setQualification(qualification);
                d.setSpecialist(specialist);
                d.setActive('Y');
                  boolean result1 = DoctorsDao.addDoctorsTable(d);
                 if(result1)
                 {
                     JOptionPane.showMessageDialog(null, "Record saved to doctors table","balle balle",JOptionPane.INFORMATION_MESSAGE); 
                 }
                

            }
            else
            {
                JOptionPane.showMessageDialog(null, "could not add doctor","error",JOptionPane.ERROR_MESSAGE);
            }

        }
        catch(SQLException s)
        {
            JOptionPane.showMessageDialog(null, "Error in database","Empty values!!",JOptionPane.ERROR_MESSAGE);
            s.printStackTrace();
        }
    }//GEN-LAST:event_btnregisterActionPerformed

