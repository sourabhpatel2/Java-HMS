       public boolean validateInput()
    {
        char[] pwd = txtpw.getPassword();
        char[] repass = txtrepwd.getPassword();
        return !(txtspecialist.getText().isEmpty()||txtqualification.getText().isEmpty()||txtuserid.getText().isEmpty()|| pwd.length<4 ||repass.length<4||txtdocid.getText().isEmpty());
    }
